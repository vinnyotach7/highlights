class News:
    def __init__(self,source,link,country,id,description):
        self.source = source
        self.link = link
        self.country = country
        self.id = id
        self.description = description


class Articles:
    def __init__(self,name,description,url,category):
        self.name = name
        self.description = description
        self.url = url
        self.url = url
        self.category = category